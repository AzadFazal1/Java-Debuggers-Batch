
public class IncrementTypes {
	public static void main(String args[]) {
		
		int x = 10;
		System.out.println(x);
		System.out.println(++x);// increment first and then assign to x
		System.out.println(x++);// display first the old value and then increment
		System.out.println(x);// display the updated value

	}
}
